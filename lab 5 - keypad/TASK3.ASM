;*****************************************
;     MDA-Win8086 EXPERIMENT PROGRAM    *
;     FILENAME  : FND.ASM
;     PROCESSOR : I8086
;     8255 TEST
;*****************************************
CODE	SEGMENT
	ASSUME	CS:CODE,DS:CODE,ES:CODE,SS:CODE
	;
PPIC_C	EQU	1FH
PPIC	EQU	1DH
PPIB	EQU	1BH
PPIA	EQU	19H
	;	
KEY	EQU	01H
        ;
        ;TASK 1
	ORG	1000H
	MOV	AL,10000000B
	OUT	PPIC_C,AL
	;
	MOV	AL,11110000B
	OUT	PPIB,AL
	;
	MOV	AL,00000000B
	OUT	PPIC,AL
	;
L2: 	MOV	SI,OFFSET DATA
        CALL	SCAN
        MOV	AL,BYTE PTR K_BUF
        AND AX, 000FH
        CMP AX, 0004H
        JA L2
        ADD SI, AX
        MOV AL, BYTE PTR CS:[SI]
        OUT PPIB, AL
	JMP	L2		
	;
	INT	3
	;		
TIMER:	MOV	CX,0
TIMER1:	NOP
	NOP
	NOP
	NOP
	LOOP	TIMER1
	RET
	;
SCAN:	IN	AL,KEY
	TEST	AL,10000000B
	JNZ	SCAN
	TEST	AL,00010000B
	JNZ	SCAN
	;
	AND	AL,00001111B
	MOV	BYTE PTR K_BUF,AL
	; key clear
	OUT	KEY,AL
	RET
	;
DATA:	DB      00000000B
        DB	00000001B
	DB	00000011B
	DB	00000111B
	DB	00001111B
	;
K_BUF:  DB 1
        ;
CODE	ENDS
	END

