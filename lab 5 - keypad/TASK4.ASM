;*****************************************
;     MDA-Win8086 EXPERIMENT PROGRAM    *
;     FILENAME  : FND.ASM
;     PROCESSOR : I8086
;     8255 TEST
;*****************************************
CODE	SEGMENT
	ASSUME	CS:CODE,DS:CODE,ES:CODE,SS:CODE
	;
PPIC_C	EQU	1FH
PPIC	EQU	1DH
PPIB	EQU	1BH
PPIA	EQU	19H
	;	
KEY	EQU	01H
        ;
        ;TASK 4
	ORG	1000H
	MOV	AL,10000000B
	OUT	PPIC_C,AL
	;
	MOV	AL,11110000B
	OUT	PPIB,AL
	;
	MOV	AL,00000000B
	OUT	PPIC,AL
	;
L1:     CALL	SCAN
        MOV	AL,BYTE PTR K_BUF
        MOV SI, OFFSET LED
        MOV DI, OFFSET SEG
        AND AX, 000FH
        CMP AX, 0005H
        JNE L1
        MOV DL, 6
L2:     MOV AL, BYTE PTR CS:[SI]
        OUT PPIB, AL
        MOV AL, BYTE PTR CS:[DI]
        OUT PPIA, AL
        CALL TIMER
        INC DI
        INC SI
        DEC DL
	JNZ	L2
        JMP L1		
	;
	INT	3
	;		
TIMER:	MOV	CX, 0000H
TIMER1:	NOP
	NOP
	NOP
	NOP
	LOOP	TIMER1
	RET
	;
SCAN:	IN	AL,KEY
	TEST	AL,10000000B
	JNZ	SCAN
	TEST	AL,00010000B
	JNZ	SCAN
	;
	AND	AL,00001111B
	MOV	BYTE PTR K_BUF,AL
	; key clear
	OUT	KEY,AL
	RET
	;
SEG:	DB	11000000B
	DB	11111001B
	DB	10100100B
	DB	10110000B
	DB	10011001B
	DB	10010010B
	;
LED:	DB      00000000B
        DB	00000001B
	DB	00000011B
	DB	00000111B
	DB	00001111B
	DB	00001111B
	;
K_BUF:  DB 1
        ;
CODE	ENDS
	END

