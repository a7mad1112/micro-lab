;*****************************************
;     MDA-Win8086 EXPERIMENT PROGRAM    *
;     FILENAME  : FND.ASM
;     PROCESSOR : I8086
;     8255 TEST
;*****************************************
CODE	SEGMENT
	ASSUME	CS:CODE,DS:CODE,ES:CODE,SS:CODE
	;
PPIC_C	EQU	1FH
PPIC	EQU	1DH
PPIB	EQU	1BH
PPIA	EQU	19H
	;	
KEY	EQU	01H
        ;
        ;TASK 1
	ORG	1000H
	MOV	AL,10000000B
	OUT	PPIC_C,AL
	;
	MOV	AL,11110000B
	OUT	PPIB,AL
	;
	MOV	AL,00000000B
	OUT	PPIC,AL
	;
L2: 	MOV	SI,OFFSET DATA
        CALL	SCAN
        MOV	AL,BYTE PTR K_BUF
        AND AX, 000FH
        ADD SI, AX
        MOV AL, BYTE PTR CS:[SI]
        OUT PPIA, AL
	JMP	L2		
	;
	INT	3
	;		
TIMER:	MOV	CX,0
TIMER1:	NOP
	NOP
	NOP
	NOP
	LOOP	TIMER1
	RET
	;
SCAN:	IN	AL,KEY
	TEST	AL,10000000B
	JNZ	SCAN
	TEST	AL,00010000B
	JNZ	SCAN
	;
	AND	AL,00001111B
	MOV	BYTE PTR K_BUF,AL
	; key clear
	OUT	KEY,AL
	RET
	;
DATA:	DB	11000000B
	DB	11111001B
	DB	10100100B
	DB	10110000B
	DB	10011001B
	DB	10010010B
	DB	10000010B
	DB	11111000B
	DB	10000000B
	DB	10010000B
	DB      10001000B;A
	DB      10000000B;B
	DB      11000110B;C
	DB      11000000B;D
	DB      10000110B;E
	DB      10001110B;F
	DB	00H
	;
K_BUF:  DB 1
        ;
CODE	ENDS
	END

