;*****************************************
;     MDA-Win8086 EXPERIMENT PROGRAM    *
;     FILENAME  : FND.ASM
;     PROCESSOR : I8086
;     8255 TEST
;*****************************************
CODE	SEGMENT
	ASSUME	CS:CODE,DS:CODE,ES:CODE,SS:CODE
	;
PPIC_C	EQU	1FH
PPIC	EQU	1DH
PPIB	EQU	1BH
PPIA	EQU	19H
	;	
KEY	EQU	01H
        ;
        ;TASK 5
	ORG	1000H
	MOV	AL,10000000B
	OUT	PPIC_C,AL
	;
	MOV	AL,11110000B
	OUT	PPIB,AL
	;
	MOV	AL,00000000B
	OUT	PPIC,AL
	;
L1:     CALL	SCAN
        MOV	AL,BYTE PTR K_BUF
        AND AX, 000FH
        CMP AX, 000AH
        JNE L1
        XOR AX, AX
        MOV DL, 25
        MOV AL, 01110111B
L2:     OUT PPIB, AL
        CALL TIMER
        ROR AL, 1
        DEC DL
	JNZ	L2
        JMP L1		
	;
	INT	3
	;		
TIMER:	MOV	CX, 0FFFH
TIMER1:	NOP
	NOP
	NOP
	NOP
	LOOP	TIMER1
	RET
	;
SCAN:	IN	AL,KEY
	TEST	AL,10000000B
	JNZ	SCAN
	TEST	AL,00010000B
	JNZ	SCAN
	;
	AND	AL,00001111B
	MOV	BYTE PTR K_BUF,AL
	; key clear
	OUT	KEY,AL
	RET
	;
K_BUF:  DB 1
        ;
CODE	ENDS
	END

